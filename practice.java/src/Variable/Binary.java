package Variable;

public class Binary {
public static void main(String[] args) {
	int a= 10;
	int b=20;
	int c=a+b;
	System.out.println(c);
	a+=b;
	System.out.println(a);
	a-=b;
	System.out.println(a);
	a*=b;
	System.out.println(a);
	a/=b;
	System.out.println(a);
	a%=b;
	System.out.println(a);
}
}
